package com.code;

public class Explicit_casting {
    public static void main(String[] args) {
        int i1=(int) 4.5;
        System.out.println(i1);
        double d1=4.5+3;
        System.out.println(d1);
       // Dividing

        System.out.println((double)1/2);
        System.out.println(1/2);
        System.out.println(1/2.0);
       // int i=1/2.0;  //Error

    }
}
